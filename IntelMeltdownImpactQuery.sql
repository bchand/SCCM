Select Distinct 
 sys.Name0 AS SystemName
,replace(replace(replace(replace(replace(replace(replace(replace(LEFT(processor.Name0,CHARINDEX('@',processor.Name0)),' CPU',''),' @',''),' 0',''),'  ',' '),'  ',' '),'  ',' '),'  ',' '),'E7- ','E7-') AS Processor
,OS.Caption0 AS OSName
INTO #PCProcOS
From v_R_System sys
INNER JOIN v_GS_PROCESSOR processor ON processor.ResourceID = sys.ResourceID
INNER JOIN v_GS_OPERATING_SYSTEM OS ON OS.ResourceID = sys.ResourceID
Where LEN(processor.Name0) > 5;

