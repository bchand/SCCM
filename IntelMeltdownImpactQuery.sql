Select   #PCProcOS.SystemName
		,#PCProcOS.OSName
		,#PCProcOS.Processor
		,ISNULL(#Processors.CodeName,'Unknown') AS CodeName
		,ISNULL(#Impact.Impact,'Unknown') AS Impact
		,CASE 
			WHEN #PCProcOS.OSName Like '%Windows 10%' AND #Impact.Impact = 'High' THEN 'Potentially Noticed'
			WHEN #PCProcOS.OSName Like '%Windows 10%' AND #Impact.Impact = 'Low' THEN 'Low'
			WHEN #PCProcOS.OSName Like '%Windows 7%' AND #Impact.Impact = 'High' THEN 'Noticeable'
			WHEN #PCProcOS.OSName Like '%Windows 7%' AND #Impact.Impact = 'Low' THEN 'Potentially Noticed'
			WHEN #PCProcOS.OSName Like '%Windows 8%' AND #Impact.Impact = 'High' THEN 'Noticeable'
			WHEN #PCProcOS.OSName Like '%Windows 8%' AND #Impact.Impact = 'Low' THEN 'Potentially Noticed'
			WHEN #PCProcOS.OSName Like '%Server%' AND #Impact.Impact = 'High' THEN 'Potentially High'
			WHEN #PCProcOS.OSName Like '%Server%' AND #Impact.Impact = 'Low' THEN 'Potentially Low'
		    Else 'Unknown'
		End AS CalculatedImpact
From #PCProcOS
LEFT JOIN #Processors ON #Processors.ProcName = #PCProcOS.Processor
LEFT JOIN #Impact ON #Impact.Codename = #Processors.Codename;

Go

